'use strict';

function Menu(server) {
	
	this.getLoginInfo = function(goal, callback) {
		// init Login anchor
		var uri = window.location.origin + goal;
		server.call('GET', '/app/login?requestUri=' + uri, function(loginInfo) {
			callback(loginInfo);
		});
	};
	
}